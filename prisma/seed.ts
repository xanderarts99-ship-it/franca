async function main() {
  // Admin user
  const passwordHash = await bcrypt.hash(ADMIN_PASSWORD, 12);
  await prisma.adminUser.upsert({
    where: { email: ADMIN_EMAIL },
    update: { passwordHash },
    create: { email: ADMIN_EMAIL, passwordHash },
  });
  console.log(`✅ Admin user: ${ADMIN_EMAIL}`);

  // Properties — delete all first so re-runs are idempotent
  await prisma.property.deleteMany();
  const created = await prisma.property.createMany({ data: properties });
  console.log(`✅ Properties seeded: ${created.count}`);

  // Confirm first two
  const first2 = await prisma.property.findMany({
    take: 2,
    orderBy: { createdAt: "asc" },
    select: { name: true, coverImageUrl: true },
  });
  first2.forEach((p, i) => {
    console.log(`\n[${i + 1}] ${p.name}`);
    console.log(`    ${p.coverImageUrl}`);
  });
}

